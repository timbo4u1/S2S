import numpy as np
import json
import os
import sys
import pickle
from pathlib import Path


def load_curriculum(experiments_dir="experiments"):
    """Load curriculum data generated by Module 3"""
    X_path = os.path.join(experiments_dir, "curriculum_dataset.npy")
    y_score_path = os.path.join(experiments_dir, "curriculum_labels.npy")
    y_tier_path = os.path.join(experiments_dir, "curriculum_tiers.npy")

    if not all(os.path.exists(p) for p in [X_path, y_score_path, y_tier_path]):
        print("Curriculum files not found. Run Module 3 first.")
        print(f"Expected in: {experiments_dir}/")
        return None, None, None

    X = np.load(X_path)
    y_score = np.load(y_score_path)
    y_tier = np.load(y_tier_path)
    print(f"Loaded curriculum: {X.shape[0]} samples, {X.shape[1]} features")
    return X, y_score, y_tier


def encode_tiers(y_tier):
    """Convert tier strings to numeric labels"""
    tier_map = {'GOLD': 3, 'SILVER': 2, 'BRONZE': 1, 'REJECTED': 0, 'ERROR': 0}
    return np.array([tier_map.get(t, 0) for t in y_tier])


def train_sklearn_baseline(X, y_tier_encoded, X_val, y_val_encoded):
    """
    Train scikit-learn baseline — runs on MacBook Air.
    Fast, interpretable, no GPU needed.
    """
    from sklearn.ensemble import RandomForestClassifier, GradientBoostingClassifier
    from sklearn.preprocessing import StandardScaler
    from sklearn.metrics import classification_report, accuracy_score
    from sklearn.pipeline import Pipeline

    models = {
        "RandomForest": RandomForestClassifier(
            n_estimators=100, max_depth=10, random_state=42, n_jobs=-1
        ),
        "GradientBoosting": GradientBoostingClassifier(
            n_estimators=100, max_depth=5, random_state=42
        ),
    }

    results = {}
    best_model = None
    best_acc = 0

    for name, model in models.items():
        print(f"\nTraining {name}...")
        pipeline = Pipeline([
            ('scaler', StandardScaler()),
            ('model', model)
        ])
        pipeline.fit(X, y_tier_encoded)
        y_pred = pipeline.predict(X_val)
        acc = accuracy_score(y_val_encoded, y_pred)
        print(f"  Validation accuracy: {acc:.3f}")
        print(classification_report(
            y_val_encoded, y_pred,
            target_names=['REJECTED', 'BRONZE', 'SILVER', 'GOLD'],
            zero_division=0
        ))
        results[name] = {"accuracy": float(acc)}

        if acc > best_acc:
            best_acc = acc
            best_model = pipeline

    return best_model, results


def train_pytorch_model(X, y_score, X_val, y_val_score):
    """
    Train PyTorch regression model — better for Colab GPU.
    Predicts continuous quality score (0-100) instead of discrete tier.
    More useful for soft filtering and loss terms.
    """
    try:
        import torch
        import torch.nn as nn
        from torch.utils.data import DataLoader, TensorDataset
    except ImportError:
        print("PyTorch not available. Using sklearn only.")
        return None, {}

    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    print(f"\nTraining PyTorch model on: {device}")

    # Normalize
    X_mean, X_std = X.mean(0), X.std(0) + 1e-8
    X_norm = (X - X_mean) / X_std
    X_val_norm = (X_val - X_mean) / X_std

    # Tensors
    X_t = torch.FloatTensor(X_norm).to(device)
    y_t = torch.FloatTensor(y_score / 100.0).to(device)  # Normalize to 0-1
    X_v = torch.FloatTensor(X_val_norm).to(device)
    y_v = torch.FloatTensor(y_val_score / 100.0).to(device)

    dataset = TensorDataset(X_t, y_t)
    loader = DataLoader(dataset, batch_size=64, shuffle=True)

    # Model: small MLP — runs anywhere
    n_features = X.shape[1]
    model = nn.Sequential(
        nn.Linear(n_features, 64),
        nn.ReLU(),
        nn.Dropout(0.2),
        nn.Linear(64, 32),
        nn.ReLU(),
        nn.Dropout(0.2),
        nn.Linear(32, 16),
        nn.ReLU(),
        nn.Linear(16, 1),
        nn.Sigmoid()  # Output 0-1
    ).to(device)

    optimizer = torch.optim.Adam(model.parameters(), lr=0.001)
    criterion = nn.MSELoss()

    train_losses = []
    val_losses = []
    n_epochs = 50

    for epoch in range(n_epochs):
        model.train()
        epoch_loss = 0
        for X_batch, y_batch in loader:
            optimizer.zero_grad()
            pred = model(X_batch).squeeze()
            loss = criterion(pred, y_batch)
            loss.backward()
            optimizer.step()
            epoch_loss += loss.item()

        # Validation
        model.eval()
        with torch.no_grad():
            val_pred = model(X_v).squeeze()
            val_loss = criterion(val_pred, y_v).item()

        train_losses.append(epoch_loss / len(loader))
        val_losses.append(val_loss)

        if (epoch + 1) % 10 == 0:
            print(f"  Epoch {epoch+1}/{n_epochs}: train={train_losses[-1]:.4f} val={val_loss:.4f}")

    # Package model with normalization params
    model_package = {
        "model": model,
        "X_mean": X_mean,
        "X_std": X_std,
        "device": str(device),
    }

    results = {
        "final_train_loss": float(train_losses[-1]),
        "final_val_loss": float(val_losses[-1]),
        "train_losses": [float(l) for l in train_losses],
        "val_losses": [float(l) for l in val_losses],
        "device": str(device),
    }

    return model_package, results


def save_model(model_package, out_path="model/s2s_quality_model.pkl"):
    """Save trained model for local inference"""
    os.makedirs(os.path.dirname(out_path), exist_ok=True)

    # Save sklearn model or pytorch model
    with open(out_path, 'wb') as f:
        pickle.dump(model_package, f)
    size_kb = os.path.getsize(out_path) / 1024
    print(f"Model saved: {out_path} ({size_kb:.1f} KB)")


def predict_quality(model_package, accel_window, gyro_window, sample_rate=50):
    """
    Run trained model on new window.
    Returns quality score 0-100 — use as soft filter or training weight.

    This is the inference function — runs on MacBook Air, no GPU needed.
    """
    # Extract same features as training
    features = []
    accel = np.array(accel_window)
    gyro = np.array(gyro_window)

    features.append(np.sqrt(np.mean(accel**2)))
    features.append(np.std(accel))
    features.append(np.max(np.abs(accel)))
    features.append(np.sqrt(np.mean(gyro**2)))
    features.append(np.std(gyro))

    if len(accel) > 3:
        jerk = np.diff(accel, n=1, axis=0) * sample_rate
        features.append(np.sqrt(np.mean(jerk**2)))
        features.append(np.percentile(np.abs(jerk), 95))
    else:
        features.extend([0.0, 0.0])

    for axis in range(3):
        fft = np.abs(np.fft.rfft(accel[:, axis]))
        freqs = np.fft.rfftfreq(len(accel), 1/sample_rate)
        peak_freq = freqs[np.argmax(fft)] if len(fft) > 0 else 0
        features.append(float(peak_freq))

    if accel.shape[1] >= 2:
        coupling = np.corrcoef(accel[:, 0], accel[:, 1])[0, 1]
        features.append(float(coupling) if not np.isnan(coupling) else 0.0)

    mean_accel = np.mean(accel, axis=0)
    features.append(float(np.linalg.norm(mean_accel)))

    hist, _ = np.histogram(accel.flatten(), bins=20)
    hist = hist / (hist.sum() + 1e-10)
    features.append(float(-np.sum(hist * np.log(hist + 1e-10))))

    X = np.array(features).reshape(1, -1)

    # Sklearn inference
    if hasattr(model_package, 'predict'):
        tier_num = model_package.predict(X)[0]
        tier_map = {3: 'GOLD', 2: 'SILVER', 1: 'BRONZE', 0: 'REJECTED'}
        return {
            "predicted_tier": tier_map.get(tier_num, 'BRONZE'),
            "predicted_score": float(tier_num * 33),
        }

    # PyTorch inference
    if isinstance(model_package, dict) and 'model' in model_package:
        try:
            import torch
            model = model_package['model']
            X_norm = (X - model_package['X_mean']) / model_package['X_std']
            X_t = torch.FloatTensor(X_norm)
            model.eval()
            with torch.no_grad():
                score = model(X_t).item() * 100
            tier = 'GOLD' if score >= 90 else 'SILVER' if score >= 78 else 'BRONZE' if score >= 52 else 'REJECTED'
            return {"predicted_tier": tier, "predicted_score": float(score)}
        except Exception as e:
            return {"predicted_tier": "ERROR", "error": str(e)}

    return {"predicted_tier": "ERROR", "error": "Unknown model format"}


def main():
    print("=" * 60)
    print("S2S Module 4 — Cloud Trainer")
    print("=" * 60)

    # Load curriculum
    X, y_score, y_tier = load_curriculum()
    if X is None:
        print("\nTo generate curriculum, run:")
        print("  python3 experiments/module3_curriculum_generator.py")
        print("\nThen upload to Google Colab and run this script there.")
        return

    y_tier_encoded = encode_tiers(y_tier)

    # Split train/val
    n = len(X)
    idx = np.random.permutation(n)
    split = int(n * 0.8)
    train_idx, val_idx = idx[:split], idx[split:]

    X_train, X_val = X[train_idx], X[val_idx]
    y_train_score, y_val_score = y_score[train_idx], y_score[val_idx]
    y_train_tier, y_val_tier = y_tier_encoded[train_idx], y_tier_encoded[val_idx]

    print(f"\nTrain: {len(X_train)} samples | Val: {len(X_val)} samples")

    all_results = {}

    # Train sklearn baseline (runs on Mac)
    print("\n--- Scikit-learn Baseline (runs on Mac) ---")
    sklearn_model, sklearn_results = train_sklearn_baseline(
        X_train, y_train_tier, X_val, y_val_tier
    )
    all_results["sklearn"] = sklearn_results

    if sklearn_model:
        save_model(sklearn_model, "model/s2s_quality_model_sklearn.pkl")

    # Train PyTorch model (better on Colab GPU)
    print("\n--- PyTorch Model (best on Colab GPU) ---")
    pytorch_package, pytorch_results = train_pytorch_model(
        X_train, y_train_score, X_val, y_val_score
    )
    all_results["pytorch"] = pytorch_results

    if pytorch_package:
        save_model(pytorch_package, "model/s2s_quality_model_pytorch.pkl")

